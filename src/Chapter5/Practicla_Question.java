class IPTV extends ColorTV{
	private String address;
	
	public IPTV(int size , int color , String address) {
		super(size,color);
		this.address = address;
	}
	
	@Override
	void printProperty() {
		// TODO Auto-generated method stub	
		System.out.print("나의 IPTV는 " + this.address +"주소의 ");
		super.printProperty(); 
	}
}
